String myString = new String("hello");    // Sets myString to point to a new String object instance that
                                          // contains "hello".

String myString = "hello";                // Special case of String that does the same as the new keyword.

String myString1 = "goodbye";             // Now we wave two instances of the object String, one containing
                                          // "hello" and one containing "goodbye".

String myString1 = myString;              // myString1 now points to the same instance of a String object
                                          // containing "hello" that myString points to.
